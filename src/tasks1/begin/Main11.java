package tasks1.begin;

public class Main11 {
    public static void main(String[] args) {
        float a = -35f;
        float b = 11f;
        System.out.println("Сумма чисел " + a + " и " + b + " = " + (Math.abs(a) + Math.abs(b)));
        System.out.println("Разность чисел " + a + " и " + b + " = " + (Math.abs(a) - Math.abs(b)));
        System.out.println("Произведение чисел " + a + " и " + b + " = " + (Math.abs(a) * Math.abs(b)));
        System.out.println("Частное квадротов чисел " + a + " и " + b + " = " + (Math.abs(a) / Math.abs(b)));
    }
}
